public class _03Variables {
  
    public static void main(String[ ]args){
        
        //宣告變數, 指定運算

        char grade='A';  //=等號是指定運算子
        int age = 25 ; //integer 整數型資料
        double salary = 3000.5 ; //雙精準度浮點數
        boolean isEmployed = true ; //布林值

        grade = 'B';
        age=45;
        

        System.out.println(grade); //括號內直接放變數名稱
        System.out.println(age);
        System.out.println(salary);
        System.out.println(isEmployed);
        




    }



}
